// ============================================================
// DURATIONS (Animations)
// ============================================================

class AppDurations {
  /// 200ms — hover, border color changes
  final Duration fast = const Duration(milliseconds: 200);

  /// 300ms — button transitions, card reveals
  final Duration normal = const Duration(milliseconds: 300);

  /// 400ms — screen fade in, slide up
  final Duration medium = const Duration(milliseconds: 400);

  /// 500ms — feedback card slide up
  final Duration slow = const Duration(milliseconds: 500);

  /// 1000ms — score bar fill animation
  final Duration scoreBar = const Duration(milliseconds: 1000);

  /// 1200ms — dot bounce loop
  final Duration dotBounce = const Duration(milliseconds: 1200);

  /// 1500ms — mic pulse animation
  final Duration micPulse = const Duration(milliseconds: 1500);

  /// 2200ms — splash auto navigate delay
  final Duration splash = const Duration(milliseconds: 2200);

  /// 3000ms — glow pulse
  final Duration glowPulse = const Duration(milliseconds: 3000);
}
